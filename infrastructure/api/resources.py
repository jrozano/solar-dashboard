@api_bp.route('/keys', methods=['POST'])
@login_required
@inject
def keys_create(mediator: Mediator = Provide['mediator']):
    """Create a new API key
    ---
    tags:
      - API Keys
    security:
      - bearerAuth: []
      - apiKeyHeader: []
    parameters:
      - in: body
        name: body
        required: false
        schema:
          type: object
          properties:
            name:
              type: string
              description: Descriptive name for the key (1-64 characters)
              default: "default"
    responses:
      201:
        description: API key created successfully. The raw key is returned only in this response and cannot be retrieved later.
        schema:
          type: object
          properties:
            id:
              type: string
              description: Stable key identifier (SHA-256 hash)
            key:
              type: string
              description: Full raw API key (shown only once)
            name:
              type: string
            created_at:
              type: string
              format: date-time
      400:
        description: Invalid input data
      415:
        description: Content-Type is not application/json
      401:
        description: Authentication required
    """
    if not request.is_json:
        return jsonify({'error': 'Content-Type must be application/json'}), 415
    data = request.get_json() or {}
    try:
        validated = KeyCreateSchema().load(data)
    except ValidationError as exc:
        return jsonify({'error': exc.messages}), 400
    name = validated['name']
    raw_key, entry = mediator.send(
        CreateApiKey(user_id=g.current_user['id'], name=name),
    )
    resp = entry.to_dict()
    resp['key'] = raw_key          # raw key returned only at creation time
    return jsonify(resp), 201


@api_bp.route('/keys/<key_id>', methods=['DELETE'])
@login_required
@inject
def keys_delete(key_id: str, mediator: Mediator = Provide['mediator']):
    """Revoke an API key
    ---
    tags:
      - API Keys
    security:
      - bearerAuth: []
      - apiKeyHeader: []
    parameters:
      - in: path
        name: key_id
        type: string
        required: true
        description: The key identifier (SHA-256 hash) to revoke
    responses:
      200:
        description: Key revoked successfully
        schema:
          type: object
          properties:
            deleted:
              type: boolean
      404:
        description: Key not found
        schema:
          type: object
          properties:
            message:
              type: string
      401:
        description: Authentication required
    """
    deleted = mediator.send(
        DeleteApiKey(user_id=g.current_user['id'], key_id=key_id),
    )
    if deleted:
        return jsonify({'deleted': True})
    return jsonify({'message': 'Key not found'}), 404
