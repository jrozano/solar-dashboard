"""REST API resources - infrastructure layer.

All API endpoints are route functions registered on the ``api_bp``
Blueprint (prefix ``/api``).  Dependencies are injected automatically
by the ``dependency-injector`` container.

Controllers delegate all business logic to domain command/query
handlers via the Mediator pattern — they never access repositories
directly.

The blueprint is imported and registered from the composition root.
"""

import logging

from flask import Blueprint, g, jsonify, request
from marshmallow import Schema, ValidationError, fields, validate

from dependency_injector.wiring import inject, Provide

from domain.commands.get_stats import GetStats
from domain.commands.list_alerts import ListAlerts
from domain.commands.clear_alerts import ClearAlerts
from domain.commands.list_keys import ListKeys
from domain.commands.create_api_key import CreateApiKey
from domain.commands.delete_api_key import DeleteApiKey
from infrastructure.api.auth import login_required
from mediatr import Mediator

LOG = logging.getLogger('infrastructure.api.resources')

api_bp = Blueprint('api', __name__, url_prefix='/api')


# Input schemas

class KeyCreateSchema(Schema):
    """Validates the JSON body for ``POST /api/keys``."""
    name = fields.Str(
        load_default='default',
        validate=[
            validate.Length(min=1, max=64, error='Name must be between 1 and 64 characters.'),
            validate.Regexp(
                r'^[\w\s\-]+$',
                error='Name may only contain letters, digits, spaces, underscores and hyphens.',
            ),
        ],
    )


# User info

@api_bp.route('/me')
def me():
    """Get current user information
    ---
    tags:
      - Users
    security:
      - bearerAuth: []
    responses:
      200:
        description: Authenticated user
        schema:
          type: object
          properties:
            authenticated:
              type: boolean
              example: true
            name:
              type: string
              example: "John Doe"
            email:
              type: string
              example: "john@example.com"
            picture:
              type: string
              example: "https://example.com/photo.jpg"
      401:
        description: Not authenticated
        schema:
          type: object
          properties:
            authenticated:
              type: boolean
              example: false
    """
    user = g.current_user
    if user:
        return jsonify({
            'authenticated': True,
            'name': user['name'],
            'email': user['email'],
            'picture': user['picture'],
        })
    return jsonify({'authenticated': False}), 401


